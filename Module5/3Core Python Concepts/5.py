# 5.    How does the Python code structure work?

# 1. Comments: Used to explain code.    Comments (#) explain what each part does.

# 2. Variables & Data Types     Variables (name, age, height) store different data types.
name = 'Kajal'  # String
age = 22  # Integer
height = 5.2  # Float

# 3. Conditional Statements     Conditional Statement (if age >= 18:) checks if a person is an adult or minor.
if age >= 18:
    print('You are an adult.')
else:
    print('You are a minor.')

# 4. Loops: Printing numbers from 1 to 5 using a for loop       Loop (for i in range(1, 6)) prints numbers from 1 to 5.
print('Counting: ')
for i in range(1, 6):   
    print(i)
